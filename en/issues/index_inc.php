	<p class="news-item">
 <a href="#"><b>New scientific journal "Dagestan Electronic Mathematical Reports" is established</b></a>&nbsp;<br>
By resolution of the Dagestan Scientific Center of the Russian Academy of Sciences, an electronic scientific journal "Dagestan Electronic Mathematical Reports" 
was established.
The journal is aimed at the rapid electronic publication of original research papers in the modern areas of mathematics and computer science. 
The Journal is registered as online edition in the Federal Service for Supervision of Communications, Information Technology and Communications on February 8, 2013 (certificate number: Эл № ФС77-52776). 
