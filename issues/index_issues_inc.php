 <a href="#"><b>"Дагестанские Электронные Математические Известия" объявляют о приеме статей для публикации</b></a> <br>
		 Электронному научному&nbsp;журналу "Дагестанские Электронные Математические Известия" присвоен номер ISSN: 2500-3240. Журнал включен в Российский индекс научного цитирования (РИНЦ), а также в базу общероссийского&nbsp;математического портала MathNet.Ru.&nbsp;В настоящее время редакция готовится к началу&nbsp;выпуска журнала с 2014 г., в связи с чем объявляется приём научных статей.&nbsp;С правилами для авторов можно ознакомиться <a href="http://mathreports.ru/dlya-avtorov/">в следующем разделе</a>.<br>
 <b>Дата публикации:</b> 30.09.2013
	</p>
