 <a href="#"><b>Учрежден журнал "Дагестанские Электронные Математические Известия"</b></a>&nbsp;<br>
		 Постановлением Дагестанского научного центра РАН учрежден электронный научный журнал "Дагестанские Электронные Математические Известия", ориентированный&nbsp;на быструю электронную публикацию научных статей по актуальным направлениям математики и информатики. Журнал зарегистрирован в качестве сетевого издания в Федеральной службе по надзору в сфере связи, информационных технологий и массовых коммуникаций 8 февраля 2013 г. (Номер свидетельства: Эл № ФС77-52776).&nbsp;
